package objectsworkshop;

public class KindFruit{
	
	protected boolean acids;
	protected boolean semiAcid;
	protected boolean Neutrals;
	protected boolean sweets;
	
	public boolean isAcids() {
		return acids;
	}
	public void setAcids(boolean acids) {
		this.acids = acids;
	}
	public boolean isSemiAcid() {
		return semiAcid;
	}
	public void setSemiAcid(boolean semiAcid) {
		this.semiAcid = semiAcid;
	}
	public boolean isNeutrals() {
		return Neutrals;
	}
	public void setNeutrals(boolean neutrals) {
		Neutrals = neutrals;
	}
	public boolean isSweets() {
		return sweets;
	}
	public void setSweets(boolean sweets) {
		this.sweets = sweets;
	}
	
	

}
